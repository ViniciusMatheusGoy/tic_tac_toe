    private void verifica(){
        String  b11=btn11.getText()
               ,b12=btn12.getText()
               ,b13=btn13.getText()
               ,b21=btn21.getText()
               ,b22=btn22.getText()        
               ,b23=btn23.getText()
               ,b31=btn31.getText()        
               ,b32=btn32.getText()        
               ,b33=btn33.getText();        
        if(b11.equals(b12)&&b12.equals(b13)){
            lblResult.setText(b11);
        }
        if(b11==b22&&b22==b33){
            lblResult.setText(b11);
        }
        if(b11==b21&&b21==b31){
            lblResult.setText(b11);
        }
        if(b21==b22&&b22==b23){
            lblResult.setText(b21);
        }
        if(b31==b32&&b32==b33){
            lblResult.setText(b31);
        }
        if(b12==b22&&b22==b32){
            lblResult.setText(b12);
        }
        if(b13==b23&&b23==b33){
            lblResult.setText(b13);
        }
        if(b13==b22&&b22==b31){
            lblResult.setText(b13);
        }
        if(!lblResult.getText().isBlank()){
            disableBtn();
        }
    }
    
